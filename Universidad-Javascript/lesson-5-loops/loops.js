// While
let count = 0;
while ( count < 3 ) {
    console.log( `Loop while ${count}` );
    count++
}
console.log("End loop while");

// Do While
count = 0;
do {
    console.log( `Loop do while ${count}` );
    count++
}while( count < 3 );
console.log("End loop do while");

// For
for (let i = 0; i < 3; i++) {
    console.log(`Loop for ${i}`);  
}
console.log("End loop for");